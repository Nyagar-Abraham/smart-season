Deployment Plan for Smart Season Field Monitoring
This plan outlines a step-by-step guide to deploying your application for public use using your GoDaddy domain. We will use a modern, robust architecture: Vercel (for the Frontend), Render or DigitalOcean App Platform (for the Backend), and AWS (for S3 storage, managed via your existing Terraform files).
1. Infrastructure Setup (AWS S3)
Since you already have Terraform files in the infrastructure folder, the first step is to provision your storage.
1.
Initialize Terraform: Run terraform init and terraform apply within your infrastructure folder to create the S3 bucket and IAM policies.
2.
IAM Credentials: Create an IAM User in the AWS Console with the smart-season-images-bucket-access policy created by Terraform. Save the Access Key ID and Secret Access Key.
2. Backend Deployment (Node.js API)
The backend needs a persistent environment. Render is recommended for its ease of use and native PostgreSQL support.
1.
Database: Create a managed PostgreSQL database on Render. Copy the Internal Database URL.
2.
Service Creation: Connect your GitHub repository to Render and create a new Web Service.
3.
Environment Variables: Configure the following in the Render dashboard:
◦
DATABASE_URL: Your Render PostgreSQL URL.
◦
PORT: 3000
◦
NODE_ENV: production
◦
AWS_ACCESS_KEY_ID / AWS_SECRET_ACCESS_KEY: From Step 1.
◦
AWS_REGION: (e.g., us-east-1)
◦
S3_BUCKET_NAME: The bucket name generated by Terraform.
◦
COOKIE_PARSER_SECRET: A secure random string.
4.
Health Check: Ensure the root route / returns "Smart Season Field Monitoring Service" to confirm the backend is live.
3. Frontend Deployment (React/Vite)
Vercel is the best choice for Vite-based TanStack Router applications.
1.
Project Import: Import your GitHub repository into Vercel.
2.
Build Settings:
◦
Framework Preset: Vite
◦
Root Directory: frontend
◦
Build Command: pnpm build
◦
Output Directory: dist
3.
Environment Variables:
◦
VITE_API_URL: The URL of your live Render backend (e.g., https://smart-season-backend.onrender.com).
4.
Deployment: Click Deploy. Vercel will provide a temporary .vercel.app URL.
4. Domain Configuration (GoDaddy)
Now, connect your professional domain.
1.
Frontend (Primary Domain):
◦
In Vercel, go to Settings > Domains and add yourdomain.com.
◦
Vercel will provide an A Record or CNAME.
◦
Log in to GoDaddy DNS Management and update your records to match Vercel's requirements.
2.
Backend (API Subdomain):
◦
It is best practice to host the API on a subdomain like api.yourdomain.com.
◦
In Render, go to Settings > Custom Domains and add api.yourdomain.com.
◦
Add the provided CNAME record in your GoDaddy DNS settings.
5. Security & Final Polish
1.
CORS Update: Once your domains are active, update the CORS_ORIGIN in your Backend environment variables to https://yourdomain.com.
2.
SSL: Both Vercel and Render provide automatic SSL (HTTPS) certificates. Verify that both URLs load over HTTPS.
3.
Logging: Check your Render logs to ensure the Pino/Morgan integration you implemented is correctly writing to the logs/ directory in the persistent volume (if configured) or standard output.
6. Maintenance Checklist
•
Database Migrations: Ensure you run pnpm drizzle-kit push (or a migration script) against the production database during deployment.
•
Monitoring: Set up a free monitoring tool like UptimeRobot to ping your API and Frontend every 5 minutes to ensure they stay awake.